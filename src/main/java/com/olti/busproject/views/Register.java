    protected void insertIntoDbAfterValidations() {
        regDto.setUsername(username.getText().trim());
        regDto.setEmail(email.getText().trim());
        regDto.setPassword(password.getText().trim());
        regDto.setId_card_number(idCard.getText().trim());

        if (regDto.getId_card_number() != null && regDto.getEmail() != null && regDto.getPassword() != null) {
            if (crud.isUserInDatabase(regDto.getEmail())) {
                JOptionPane.showMessageDialog(null, "Thiss user exist in database");
            } else {
                crud.createUserWithUsernameEmailPasswIdCard(regDto.getUsername(), regDto.getPassword(), regDto.getEmail(), regDto.getId_card_number());
                loginView.setVisible(true);
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please fill and add right credentions");
        }
    }

